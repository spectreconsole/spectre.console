namespace Spectre.Console
{
    /// <summary>
    /// Represents different kinds of borders.
    /// </summary>
    public enum BorderKind
    {
        /// <summary>
        /// No border.
        /// </summary>
        None = 0,

        /// <summary>
        /// A square border.
        /// </summary>
        Square = 1,

        /// <summary>
        /// An old school ASCII border.
        /// </summary>
        Ascii = 2,
    }
}
